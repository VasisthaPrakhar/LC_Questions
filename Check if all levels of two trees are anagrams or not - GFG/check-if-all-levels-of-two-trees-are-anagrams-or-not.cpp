class Solution{
    public:
    bool areAnagrams(Node *root1, Node *root2)
    {
        map<int,map<int,int>>m;
        queue<Node*>q;
        q.push(root1);
        int ans=0;
        while(!q.empty()){
            int sz=q.size();
            map<int,int>m1;
            while(sz--){
                auto node=q.front();
                m1[node->data]++;
                q.pop();
                if(node->left){
                    q.push(node->left);
                }
                if(node->right){
                    q.push(node->right);
                }
            }
            m[ans]=m1;
            ans++;
        }
        q.push(root2);
        ans=0;
        while(!q.empty()){
            int sz=q.size();
            while(sz--){
                auto node=q.front();
                if(!m[ans].count(node->data)){
                    return false;
                }
                m[ans][node->data]--;
                if(m[ans][node->data]==0){
                    m[ans].erase(node->data);
                }
                q.pop();
                if(node->left){
                    q.push(node->left);
                }
                if(node->right){
                    q.push(node->right);
                }
            }
            if(m[ans].size()>0){
                return false;
            }
            m.erase(ans);
            ans++;
        }
        return m.size()==0;
    }
};

